
import java.util.Scanner; 
   
 public class Division { 
     public static void main(String args[]) {         
         //Declaración de variables 
 		int numero1; 
 		int numero2; 
 		Scanner teclado;  
 		 
 		//Inicialización de variables e constantes 
 		teclado = new Scanner(System.in); 
 		 
 		//Lectura de datos 
 		System.out.print("Introduza un número enteiro: "); 
 		numero1 = teclado.nextInt(); 
 		System.out.print("Introduza un número enteiro: "); 
 		numero2 = teclado.nextInt(); 
 		//Realización de cálculos	 
 		 
         //Saída e finalización 
 		System.out.println("A división de " + numero1 + "/" + numero2 + " = " + numero1/numero2); 
     } 
 } 












 



 






 